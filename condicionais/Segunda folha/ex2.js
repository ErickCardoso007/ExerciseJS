/* 2) Faça um programa que receba três números, calcule e
mostre a multiplicação entre eles. */

let num1 = parseFloat(prompt('Digite um número'))
let num2 = parseFloat(prompt('Digite um número'))
let num3 = parseFloat(prompt('Digite um número'))

let multiplica = num1 * num2 * num3
alert(num1 + ' * ' + num2 + ' * ' + num3 + ' = ' + multiplica)